let lado = 5;
let largura = 16;
let comprimento = 6;
let base = 12;
let altura = 8;
let resposta = 0;

// quadrado lado * lado
resposta = lado * lado;
console.log(`A área do quadrado é ${resposta}`);
// retangulo = largura * comprimento
resposta = largura * comprimento;
console.log(`A área do retangulo é ${resposta}`);
//triangulo = base * altura / 2
resposta = (base * altura) / 2;
console.log(`A área do triangulo é ${resposta}`);
